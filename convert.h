#pragma once

// a file with conversions to various variable types as functions for use when a conversion is required to avoid additional variables

// returns given value as float
float asFloat(float value) {
	return value;
}

// returns given value as double
double asDouble(double value) {
	return value;
}

// returns given value as short
short asShort(short value) {
	return value;
}

// returns given value as unsigned short
unsigned short asUShort(unsigned short value) {
	return value;
}

// returns given value as int
int asInt(int value) {
	return value;
}

// returns given value as unsigned int
unsigned int asUInt(unsigned int value) {
	return value;
}

// returns given value as long
long asLong(long value) {
	return value;
}

// returns given value as unsigned long
unsigned long asULong(unsigned long value) {
	return value;
}

// returns given value as char
char asChar(char value) {
	return value;
}

// returns given value as wide char type
wchar_t asWchar_t(wchar_t value) {
	return value;
}

